function setup() {
  createCanvas(800, 800);
  noStroke(); // 모든 도형의 테두리 제거

  // 1. 배경색
  background(225, 240, 255);

  // 2. 왼쪽 하단의 큰 L자형 블록
  fill(173, 216, 230, 200);
  rect(0, 200, 600, 600); // 아래쪽 넓은 사각형
  rect(0, 0, 200, 800);   // 왼쪽 긴 사각형

  // 3. 왼쪽 상단의 사각형
  fill(135, 206, 250, 180);
  rect(0, 0, 400, 400);

  // 4. 중앙의 진한 파란색 사각형
  fill(0, 70, 180);
  rect(200, 50, 550, 150);  // 상단
  rect(200, 450, 550, 150); // 하단
  rect(200, 200, 150, 250); // 왼쪽
  rect(600, 200, 150, 250); // 오른쪽

  // 5. 중앙의 흰색 사각형
  fill(245, 245, 245, 220);
  rect(350, 200, 250, 250);

  // 6. 가로, 세로 노란색 선
  fill(255, 255, 150, 120);
  rect(0, 325, 800, 25); // 가로선
  rect(325, 0, 25, 800); // 세로선
}